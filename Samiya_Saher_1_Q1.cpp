// Exercise 1: Print a big letter H
// This program uses cout to display the letter H using
// characters arranged to form a large H shape.

#include <iostream>

int main() {
    // Each line of the letter H is printed using spaces and 'H' characters
    std::cout << "H     H" << std::endl;
    std::cout << "H     H" << std::endl;
    std::cout << "HH  HH" << std::endl;
    std::cout << "H     H" << std::endl;
    std::cout << "H     H" << std::endl;

    return 0;
}
